from random import randint


def jogar():
    abertura()
    palavra_secreta = sorteio_palavra()

    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while not enforcou and not acertou:

        chute = chute_jogador()

        if chute in palavra_secreta:
            chute_certo(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = '_' not in letras_acertadas
        print(letras_acertadas)

    if acertou:
        msg_vitoria()
    else:
        msg_derrota(palavra_secreta)


def abertura():
    print('-' * 27)
    print('Bem vindo ao jogo de forca!')
    print('-' * 27)


def sorteio_palavra():
    arquivo = open('palavras.txt', 'r')
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = randint(0, len(palavras))
    palavra_secreta = palavras[numero]
    return palavra_secreta


def inicializa_letras_acertadas(palavra):
    return ['_' for letra in palavra]


def chute_jogador():
    chute = str(input('Qual letra? ')).upper().strip()
    return chute


def chute_certo(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1


